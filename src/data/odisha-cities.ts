// Get all postal codes for Odisha
export const getAllOdishaPostalCodes = (): string[] => {
  const allCodes: string[] = [];
  ODISHA_CITIES.forEach(city => {
    allCodes.push(...city.postalCodes);
  });
  return [...new Set(allCodes)].sort();
};

// Validate if a postal code belongs to Odisha
export const isValidOdishaPostalCode = (postalCode: string): boolean => {
  const allCodes = getAllOdishaPostalCodes();
  return allCodes.includes(postalCode);
};

// Get city by postal code
export const getCityByPostalCode = (postalCode: string): OdishaCity | null => {
  return ODISHA_CITIES.find(city => 
    city.postalCodes.includes(postalCode)
  ) || null;
};

// Get all city names sorted alphabetically
export const getAllCityNames = (): string[] => {
  return ODISHA_CITIES.map(city => city.name).sort();
};
